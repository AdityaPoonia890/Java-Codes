public class Switch {
    public static void main(String[] args) {
        int n=2;
        switch(n){
            case 1:System.out.println("mon");
            
            case 2:System.out.println("tue");
            break;
            case 3:System.out.println("wedd");
            break;
            default:System.out.println("no");
        }
    }
    
}
